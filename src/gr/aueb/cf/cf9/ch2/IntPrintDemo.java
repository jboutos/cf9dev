public class IntPrintDemo {
    public static void main(String[] args) {
        int i = 12;

        System.out.print(i);
        System.out.println(i);
        System.out.printf("%d", i);

        System.out.print("Η τιμή του i είναι: " + i);
        System.out.println("Η τιμή του i είναι: " + i);
        System.out.printf("Η τιμή του i είναι: %d", i);
    }
}
