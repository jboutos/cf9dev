package gr.aueb.cf.cf9.ch2;

/**
 * Έτοιμη κλάση Math
 */

public class MathDemo {
    public static void main(String[] args) {
        System.out.println("Το απόλυτο του -5 είναι: " + Math.abs(-5));

        //.min and .max accept two variables
        System.out.println("Το ελάχιστο μεταξύ του 1 και του 10 είναι: " + Math.min(1, 10));
        System.out.println("Το μέγιστο μεταξύ του 1 και του 10 είναι: " + Math.max(1, 10));
    }
}
